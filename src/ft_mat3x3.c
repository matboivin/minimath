#include "minimath.h"

/*
** 3x3 Matrice
** 
** [0, 1, 2,
**  3, 4, 5,
**  6, 7, 8]
*/

void	create_mat3x3(t_mat_3x3 result, t_vec3 a, t_vec3 b, t_vec3 c)
{
	result[0] = a;
	result[1] = b;
	result[2] = c;
}
